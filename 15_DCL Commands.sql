DCL : Data Control Language
-----------------------------------
GRANT : This command is Used to grant privileges /user permission.
---------
Syntax:
If you wanted to grant only SELECT access on your table to all users, you could grant the privileges to the public keyword.
--GRANT SELECT ON suppliers TO public;
----====================
REVOKE: This command is Used to take back privileges/user permission. 
---------
Syntax:
----====================
The syntax for revoking privileges on a table in Oracle is:
--REVOKE privileges ON object FROM user;
